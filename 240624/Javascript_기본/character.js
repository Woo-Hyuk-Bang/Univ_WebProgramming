console.log("This is 'String'")
console.log('This is "String"')

// 이스케이프 문자형 - 역슬래시형 문자
console.log("This is \"String\"")
console.log("This is \'String\'")

// 이스케이프 문자형 2 \t, \n, \\ 활용
console.log("이름\t나이");
console.log("안녕\n하세요");
console.log("\\\\");

// 문자열 합하기
console.log("가나다" + "라마" + "바사아" + "자차카타" + "파하");

// 문자 선택 연산자 - 인덱스를 활용한 출력방법
console.log("안녕하세요"[0]);       // 안
console.log("안녕하세요"[1]);       // 녕
console.log("안녕하세요"[3]);       // 세

// 템플릿 문자열[ECMAScript6]
console.log('안녕하세요');
